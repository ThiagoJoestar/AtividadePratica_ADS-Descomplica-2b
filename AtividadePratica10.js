// Math.sqrt
let raiz = Math.sqrt(4);
console.log("Raiz quadrade de 4 => " + raiz); 

// Math.max e Math.min
let num = [2, 4, 6, 8, 10];
let maior = Math.max(...num);
let menor = Math.min(...num);
console.log("O maior número da lista é " + maior);
console.log("O menor número da lista é " + menor);

// Math.pow
let base = 3;
let expoente = 4;
let potencia = Math.pow(base, expoente);
console.log(base + " elevado a " + expoente + " ª potência é: " + potencia);